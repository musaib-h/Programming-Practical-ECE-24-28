#include <stdio.h>

int main() {
    printf("Using `break` to stop at 5:\n");
    for (int i = 1; i <= 10; i++) {
        if (i == 5) break;
        printf("%d ", i);
    }
    printf("\n");

    printf("Using `continue` to skip 5:\n");
    for (int i = 1; i <= 10; i++) {
        if (i == 5) continue;
        printf("%d ", i);
    }
    printf("\n");
    return 0;
}
